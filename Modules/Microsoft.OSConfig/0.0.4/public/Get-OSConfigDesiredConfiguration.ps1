using namespace System.Management.Automation

function Get-OSConfigDesiredConfiguration {
    <#
    .SYNOPSIS
        Get a desired configuration setting and its metadata.

    .DESCRIPTION
        Get the metadata of a setting in a scenario. The metadata includes the name, description,
        authority, drift control, data type, and value of the setting. If no setting is specified, all
        settings in the scenario are returned. If the Raw parameter is specified, only the value of the
        setting is returned.

    .PARAMETER Scenario
        The name of the scenario.

    .PARAMETER Name
        The name of the setting.

    .PARAMETER Raw
        Return the value of the setting.

    .PARAMETER NoRefresh
        Do not refresh the setting and leave documents in place.

    .PARAMETER Authority
        The GUID or name of the authority.

    .PARAMETER Force
        The flag for proceeding without interaction.

    .EXAMPLE
        Get-OSConfigDesiredConfiguration -Scenario "MyScenario"

        Gets the metadata of all settings in the scenario.

    .EXAMPLE
        Get-OSConfigDesiredConfiguration -Scenario "MyScenario" -Name "MySetting"

        Gets the metadata of the setting in the scenario.

    .EXAMPLE
        Get-OSConfigDesiredConfiguration -Scenario "MyScenario" -Name "MySetting" -Raw

        Gets the value of the setting in the scenario.
    #>
    [OutputType([PSCustomObject])]
    [CmdletBinding()]
    [Alias("osc-get")]
    param(
        [Parameter(Mandatory)]
        [String] $Scenario,

        [Parameter()]
        [String] $Name,

        [Parameter()]
        [Switch] $Raw,

        [Parameter()]
        [Switch] $NoRefresh,

        [Parameter()]
        [String] $Authority = "E17005D5-A50E-4E57-BE94-1D4FA69C6F93",

        [Parameter()]
        [Switch] $Force
    )

    try {
        $ErrorActionPreference = "Stop"
        $ErrorRecord = $null
        $StopWatch = [System.Diagnostics.Stopwatch]::StartNew()

        Write-Notice

        Install-OSConfigScenarioDefinition -Name $Scenario -Authority $Authority -Force:$Force

        Assert-Environment

        $SourceId = ConvertTo-SourceId -Value $Authority
        Assert-SourceId -SourceId $SourceId

        $Scenario, $Version = Get-Attributes -DisplayName $Scenario
        Assert-Parameters -CmdletType DesiredConfiguration -Scenario $Scenario -Version $Version -Setting $Name

        Write-Progress -Activity "Processing" -Status $ENV:COMPUTERNAME -SecondsRemaining -1

        $Schema = Get-Schema -Scenario $Scenario -Version $Version -Name $Name

        if ($Schema.AccessType -eq [AccessType]::WriteOnly) {
            throw [InvalidValueException]::new(($Strings.ErrorWriteOnlyScenario -f $Scenario))
        }

        $Documents = Get-DocumentContent -SourceId $SourceId -Scenario $Scenario -Version $Version -Action "Get"

        $CreateNew = ($null -eq $Documents.OsConfiguration.Scenario.$Scenario.$Name)

        if (($CreateNew) -or (-not $NoRefresh)) {
            # Remove any existing documents.
            $Documents | ForEach-Object { Remove-Document -SourceId $SourceId -Document $_ }

            $Settings = @{}
            $Schema.Settings | Where-Object { (-not $Name) -or ($_.Name -eq $Name) -or ($_.Required) } | ForEach-Object {
                if (($_.Schema.Type -eq [Type]::Array) -and ($_.Schema.Items.Type -eq [Type]::Object)) {
                    # Use the auto-generated intermediate setting name to get the array of IDs.
                    if (Get-Capability -Capability ValueFormatDataType) {
                        $Settings["__$($_.Schema.Items.Name)Ids"] = @()
                    } else {
                        $Settings["__$($_.Schema.Items.Name)Ids"] = ""
                    }
                } else {
                    $Settings[$_.Name] = $_.Schema.EmptyValue()
                }
            }
            $Settings = ([PSCustomObject] $Settings)

            # Create a new document.
            $Document = New-Document -Scenario $Scenario -Settings $Settings -Version $Version -Action "Get"
            Set-Document -SourceId $SourceId -Document $Document
        }

        # Get the new document result.
        $DocumentResult = Get-DocumentResult -SourceId $SourceId -Scenario $Scenario -Version $Version -Action "Get"
        $Settings = $Schema.Settings | Where-Object { (-not $Name) -or ($_.Name -eq $Name) } | Sort-Object -Property Name

        $DriftControl = Get-DriftControl -SourceId $SourceId

        if ($Raw) {
            $Result = [PSCustomObject]@{}
        } else {
            $Result = @()
        }

        $Settings | ForEach-Object {
            $SettingName = $_.Name

            # If the schema is an array of objects, this means the document result contains all the IDs.
            # Make a document that contains all the objects and the IDs to get the actual values.
            if (($_.Schema.Type -eq [Type]::Array) -and ($_.Schema.Items.Type -eq [Type]::Object)) {
                $IntermediateName = $_.Schema.Items.Name
                $IntermediateSettingName = "$($Script:Constants.PrivateSettingPrefix)$($IntermediateName)Ids"
                $IntermediateSettingId = "$($IntermediateName)Id"
                $IntermediateValue = $DocumentResult.OsConfiguration.Scenario.$Scenario.$IntermediateSettingName
                $EmptyValue = $_.Schema.Items.EmptyValue()

                $CreateNewIntermediate = ($null -eq $Documents.OsConfiguration.Scenario.$Scenario.$IntermediateSettingName)

                if (($CreateNewIntermediate) -or (-not $NoRefresh)) {
                    # Create a new intermediate document.
                    $IntermediateSettings = @()
                    $IntermediateValue | ForEach-Object {
                        $IntermediateSettings += [PSCustomObject]@{
                            $IntermediateSettingId = $_
                            $IntermediateName      = $EmptyValue
                        }
                    }

                    $IntermediateDocument = New-Document -Scenario $Scenario -Settings @{ $SettingName = @($IntermediateSettings) } -Version $Version -Action "Get"
                    Set-Document -SourceId $SourceId -Document $IntermediateDocument
                }

                # Get the intermediate document result.
                $IntermediateDocumentResult = Get-DocumentResult -SourceId $SourceId -Scenario $Scenario -Version $Version -Setting $SettingName -Action "Get"

                $SettingValue = $IntermediateDocumentResult.OsConfiguration.Scenario.$Scenario.$SettingName
            } else {
                $SettingValue = $DocumentResult.OsConfiguration.Scenario.$Scenario.$SettingName
            }

            if ($Raw) {
                $Result | Add-Member -MemberType NoteProperty -Name $SettingName -Value $SettingValue
            } else {
                $Result += [PSCustomObject]@{
                    Name         = $_.Name
                    Description  = $_.Description
                    Authority    = $SourceId
                    DriftControl = $DriftControl
                    DataType     = $_.DataType
                    Value        = $SettingValue
                }
            }
        }

        if ($Raw -and $Name) {
            $Result.$Name
        } else {
            $Result
        }
    } catch {
        $ErrorRecord = $_
        throw
    } finally {
        $StopWatch.Stop()
        Write-Progress -Activity "Processing" -Completed
        Write-Telemetry -ErrorRecord $ErrorRecord -SourceId $SourceId -Parameters @{ Scenario = $Scenario; Version = $Version; Name = $Name; } -StopWatch $StopWatch | Out-Null
    }
}

Register-ArgumentCompleter -CommandName Get-OSConfigDesiredConfiguration -ParameterName Scenario -ScriptBlock $ScenarioCompletion
Register-ArgumentCompleter -CommandName Get-OSConfigDesiredConfiguration -ParameterName Name -ScriptBlock $SettingCompletion
Register-ArgumentCompleter -CommandName Get-OSConfigDesiredConfiguration -ParameterName Authority -ScriptBlock $AuthorityCompletion

