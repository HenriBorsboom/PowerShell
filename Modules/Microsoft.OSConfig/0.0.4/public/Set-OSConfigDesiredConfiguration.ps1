using namespace System.Management.Automation

function Set-OSConfigDesiredConfiguration {
    <#
    .SYNOPSIS
        Set a desired configuration setting.

    .DESCRIPTION
        Set a desired configuration setting. A setting can be set to a specific value or to its default
        value (defined in the scenario definition). If no setting name is provided, all settings in the
        scenario are set using the value.

    .PARAMETER Scenario
        The name of the scenario.

    .PARAMETER Name
        The name of the setting.

    .PARAMETER Value
        The value of the setting.

    .PARAMETER Default
        Use the default value of the setting.

    .PARAMETER Authority
        The GUID or name of the authority.

    .PARAMETER Force
        The flag for proceeding without interaction.

    .EXAMPLE
        Set-OSConfigDesiredConfiguration -Scenario "MyScenario" -Name "MySetting" -Value "MyValue"

        Sets the value of the setting.

    .EXAMPLE
        Set-OSConfigDesiredConfiguration -Scenario "MyScenario" -Name "MySetting" -Default

        Sets the value of the setting to its default value.

    .EXAMPLE
        Set-OSConfigDesiredConfiguration -Scenario "MyScenario" -Value @{ MySetting = "MyValue" }

        Sets the value of all settings in the scenario.

    .EXAMPLE
        Set-OSConfigDesiredConfiguration -Scenario "MyScenario" -Default

        Sets the value of all settings in the scenario to their default values.
        #>
    [OutputType([Void])]
    [CmdletBinding(SupportsShouldProcess, ConfirmImpact = "High", DefaultParameterSetName = "Value")]
    [Alias("osc-set")]
    param(
        [Parameter(Mandatory, ParameterSetName = "Value")]
        [Parameter(Mandatory, ParameterSetName = "Default")]
        [String] $Scenario,

        [Parameter(ParameterSetName = "Value")]
        [Parameter(ParameterSetName = "Default")]
        [String] $Name,

        [Parameter(Mandatory, ParameterSetName = "Value", ValueFromPipeline)]
        [Object] $Value = $null,

        [Parameter(Mandatory, ParameterSetName = "Default")]
        [Switch] $Default,

        [Parameter(ParameterSetName = "Value")]
        [Parameter(ParameterSetName = "Default")]
        [String] $Authority = "E17005D5-A50E-4E57-BE94-1D4FA69C6F93",

        [Parameter(ParameterSetName = "Value")]
        [Parameter(ParameterSetName = "Default")]
        [Switch] $Force
    )

    try {
        $ErrorActionPreference = "Stop"
        $ErrorRecord = $null
        $StopWatch = [System.Diagnostics.Stopwatch]::StartNew()

        Write-Notice

        # Check that the operation is confirmed.
        if ($Force -and (-not $Confirm)) {
            $ConfirmPreference = "None"
        }

        Install-OSConfigScenarioDefinition -Name $Scenario -Authority $Authority

        $SourceId = ConvertTo-SourceId -Value $Authority

        Assert-SourceId -SourceId $SourceId
        Assert-Environment

        $Scenario, $Version = Get-Attributes -DisplayName $Scenario

        Assert-Parameters -CmdletType DesiredConfiguration -Scenario $Scenario -Version $Version -Setting $Name

        Write-Progress -Activity "Processing" -Status $ENV:COMPUTERNAME -SecondsRemaining -1

        if (-not ($PSCmdlet.ShouldProcess((Get-Target -Scenario $Scenario -Version $Version -Setting $Name)))) {
            return
        }

        $Schema = Get-Schema -Scenario $Scenario -Version $Version -Name $Name

        if ($Schema.AccessType -eq [AccessType]::ReadOnly) {
            throw [InvalidValueException]::new($Strings.ErrorReadOnlyScenario -f $Scenario)
        }

        if ($Name) {
            $Schema = $Schema.GetSetting($Name)
        }

        if ($Schema.AccessType -eq [AccessType]::ReadOnly) {
            throw [InvalidValueException]::new($Strings.ErrorReadOnlySetting -f $Name)
        }

        if ($Default) {
            $Value = $Schema.DefaultValue()
        }

        $Value = ([PSCustomObject] $Value)
        $Value = Invoke-Transformer -Schema $Schema -Value $Value

        if ($Name) {
            if ($Schema.DataType -eq "Array") {
                $Settings = [PSCustomObject]@{ $Name = @($Value) }
            } else {
                $Settings = [PSCustomObject]@{ $Name = $Value }
            }
        } else {
            $Settings = [PSCustomObject]@{}
            $Value.PSObject.Properties | ForEach-Object {
                $SettingName = $_.Name
                $SettingValue = $_.Value
                $SettingSchema = $Schema.Settings | Where-Object { $_.Name -eq $SettingName }

                if ($SettingSchema.DataType -eq "Array") {
                    $SettingValue = @($SettingValue)
                }

                $Settings | Add-Member -MemberType NoteProperty -Name $SettingName -Value $SettingValue
            }
        }

        $Documents = Get-DocumentContent -SourceId $SourceId -Scenario $Scenario -Version $Version -Action "Set"

        if ($Documents) {
            # Update existing settings.
            $Documents | ForEach-Object {
                $Document = $_
                $Dirty = $False

                $Settings.PSObject.Properties.Copy() | ForEach-Object {
                    $SettingName = $_.Name
                    $SettingValue = $_.Value

                    if ($Document.OsConfiguration.Scenario.$Scenario.$SettingName) {
                        if ($Document.OsConfiguration.Scenario.$Scenario.$SettingName -ne $SettingValue) {
                            if ($Schema.DataType -eq "Array") {
                                $Document.OsConfiguration.Scenario.$Scenario.$SettingName += $SettingValue
                            } else {
                                $Document.OsConfiguration.Scenario.$Scenario.$SettingName = $SettingValue
                            }

                            $Dirty = $True
                        }

                        $Settings.PSObject.Properties.Remove($SettingName)
                    }
                }

                if ($Dirty) {
                    Set-Document -SourceId $SourceId -Document $Document
                }
            }

            # Add new settings.
            if ((@($Settings.PSObject.Properties).Count -gt 0)) {
                $Document = $Documents[-1]

                $Settings.PSObject.Properties | ForEach-Object {
                    $SettingName = $_.Name
                    $SettingValue = $_.Value

                    $Document.OsConfiguration.Scenario.$Scenario | Add-Member -MemberType NoteProperty -Name $SettingName -Value $SettingValue -Force
                }

                Set-Document -SourceId $SourceId -Document $Document
            }
        } else {
            $Document = New-Document -Scenario $Scenario -Settings $Settings -Version $Version -Action "Set"
            Set-Document -SourceId $SourceId -Document $Document
        }
    } catch {
        $ErrorRecord = $_
        throw
    } finally {
        $StopWatch.Stop()
        Write-Progress -Activity "Processing" -Completed
        Write-Telemetry -ErrorRecord $ErrorRecord -SourceId $SourceId -Parameters @{ Scenario = $Scenario; Version = $Version; Name = $Name; } -StopWatch $StopWatch | Out-Null
    }
}

Register-ArgumentCompleter -CommandName Set-OSConfigDesiredConfiguration -ParameterName Scenario -ScriptBlock $ScenarioCompletion
Register-ArgumentCompleter -CommandName Set-OSConfigDesiredConfiguration -ParameterName Name -ScriptBlock $SettingCompletion
Register-ArgumentCompleter -CommandName Set-OSConfigDesiredConfiguration -ParameterName Authority -ScriptBlock $AuthorityCompletion

